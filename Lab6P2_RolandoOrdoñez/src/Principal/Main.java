    public void crearDocente(){
        docente D = new docente();
        D.setNombre(jt_nombre_docente.getText());
        D.setApellido(jt_apellido_docente.getText());
        D.setUsuario(jt_usuario_docente.getText());
        D.setPassword(jt_password_docente.getText());
        D.setN_registro(jt_registro_docente.getText());
        D.setTitulo(jt_titulo_docente.getText());
        D.setImpartido_clase((Integer)js_clases.getValue());
        if (RB_salud.isSelected()){
            D.setFacultad("salud");
        }
        if (RB_licenciatura.isSelected()){
            D.setFacultad("licenciatura");
        }
        if (RB_Ingenieria.isSelected()){
            D.setFacultad("ingenieria");
        }
        docentes.add(D);
        JFileChooser jfc = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivos de texto","txt");
        jfc.addChoosableFileFilter(filtro);
        int seleccion = jfc.showSaveDialog(jd_crear_docente);
        FileWriter fw = null;
        BufferedWriter bw = null;
        if (seleccion==JFileChooser.APPROVE_OPTION){
            try{
                File fichero = null;
                if (jfc.getFileFilter().getDescription().equals("Archivos de texto")){
                    fichero = new File(jfc.getSelectedFile().getPath()+".txt");
                }
                else{
                    fichero = jfc.getSelectedFile();
                }
                fw = new FileWriter(fichero);
                bw = new BufferedWriter(fw);
                bw.write(D.toString());
                bw.flush();
            }
            catch (Exception e){
                e.printStackTrace();
            }
            try{
                bw.close();
                fw.close();
            }
            catch (IOException ex){
                
            }
        }
        jt_nombre_docente.setText("");
        jt_apellido_docente.setText("");
        jt_usuario_docente.setText("");
        jt_password_docente.setText("");
        jt_registro_docente.setText("");
        jt_titulo_docente.setText("");
    }
    
